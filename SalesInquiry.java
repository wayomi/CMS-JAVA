    public void table_Update()
    {
        try {
            String sql = "SELECT CustNo AS 'Customer No',Name,mobile AS 'Contact No', Feedback AS 'Feedback',"
                    + " CallBy AS 'Called By',date AS 'Date' FROM inquiry";
            
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            JTableHeader head = jTable_inquiry.getTableHeader();
            head.setForeground(Color.blue);            
            jTable_inquiry.getTableHeader().setFont(new Font("Times New Roman", Font.BOLD, 14));
            jTable_inquiry.setModel(DbUtils.resultSetToTableModel(rs));
            jTable_inquiry.setShowGrid(true); 
            jTable_inquiry.getColumnModel().getColumn(0).setPreferredWidth(2);
            jTable_inquiry.getColumnModel().getColumn(1).setPreferredWidth(2);
            jTable_inquiry.getColumnModel().getColumn(2).setPreferredWidth(5);
            jTable_inquiry.getColumnModel().getColumn(3).setPreferredWidth(150);
            jTable_inquiry.getColumnModel().getColumn(4).setPreferredWidth(5);
            jTable_inquiry.getColumnModel().getColumn(5).setPreferredWidth(5);
        } 
        catch (Exception e) {
            JOptionPane.showMessageDialog(null,"SalesInquiry load table \n" + e);
        }
    }
