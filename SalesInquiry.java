    private void jButton_ReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ReportActionPerformed
        String path = "";
        JFileChooser j1 = new JFileChooser();
        j1.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int x = j1.showSaveDialog(this);

        if(x==JFileChooser.APPROVE_OPTION)
        {
            path = j1.getSelectedFile().getPath();
        }
        
        try{
            Document doc = new Document();
            String filename = "Sales Inquiry Report.pdf";
            PdfWriter.getInstance(doc, new FileOutputStream(path+filename));
            doc.open();

            doc.add(new Paragraph("CMS - Sales Inquiry Report File \n", FontFactory.getFont(FontFactory.TIMES,14,com.lowagie.text.Font.BOLD)));
            doc.add(new Paragraph(new Date().toString(),FontFactory.getFont(FontFactory.TIMES,10)));            

            com.lowagie.text.Font fontH1 = new com.lowagie.text.Font(com.lowagie.text.Font.TIMES_ROMAN,8, com.lowagie.text.Font.BOLD);
            com.lowagie.text.Font fontH = new com.lowagie.text.Font(com.lowagie.text.Font.TIMES_ROMAN,7);

            //table.AddCell(new PdfPCell(new Phrase(yourDatabaseValue,fontH1)));
            PdfPTable p1 = new PdfPTable(6);
            p1.addCell(new PdfPCell(new Phrase("Customer No",fontH1)));
            p1.addCell(new PdfPCell(new Phrase("Name",fontH1)));
            p1.addCell(new PdfPCell(new Phrase("Contact No",fontH1)));
            p1.addCell(new PdfPCell(new Phrase("Feedback",fontH1)));
            p1.addCell(new PdfPCell(new Phrase("Call By",fontH1)));
            p1.addCell(new PdfPCell(new Phrase("Date",fontH1)));            

            for(int i=0; i<jTable_inquiry.getRowCount(); i++)
            {
                String CustNO = jTable_inquiry.getValueAt(i, 0).toString();
                String name = jTable_inquiry.getValueAt(i, 1).toString();
                String Contact = jTable_inquiry.getValueAt(i, 2).toString();
                String Feedback = jTable_inquiry.getValueAt(i, 3).toString();
                String Call = jTable_inquiry.getValueAt(i, 4).toString();
                String Date = jTable_inquiry.getValueAt(i, 5).toString();
                

                p1.addCell(new PdfPCell(new Phrase(CustNO,fontH)));
                p1.addCell(new PdfPCell(new Phrase(name,fontH)));
                p1.addCell(new PdfPCell(new Phrase(Contact,fontH)));
                p1.addCell(new PdfPCell(new Phrase(Feedback,fontH)));
                p1.addCell(new PdfPCell(new Phrase(Call,fontH)));
                p1.addCell(new PdfPCell(new Phrase(Date,fontH)));               

            }
            doc.add(p1);
                       
            doc.close();

            JOptionPane.showMessageDialog(null, "File Downloaded..!!");           
            
        }
            catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "Error downloading File..!!");
        }
    }//GEN-LAST:event_jButton_ReportActionPerformed

