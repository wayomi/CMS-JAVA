                String salesBy = jTable_jobcard.getValueAt(i, 4).toString();
                String type = jTable_jobcard.getValueAt(i, 5).toString();
                String total = jTable_jobcard.getValueAt(i, 6).toString();
                String status = jTable_jobcard.getValueAt(i, 7).toString();
                String created = jTable_jobcard.getValueAt(i, 8).toString();

                p1.addCell(new PdfPCell(new Phrase(OrderNo,fontH)));
