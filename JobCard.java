    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            String sql = "SELECT Max(orderId) From orderitem";
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            rs.next();
            
            rs.getString("Max(orderId)");
            
            if(rs.getString("Max(orderId)")==null)
            {
                jTextField_orderNo.setText("I0001");
            }
            else
            {
                Long id = Long.parseLong(rs.getString("Max(orderId)").substring(2,rs.getString("Max(orderId)").length()));
                id++;
                jTextField_orderNo.setText("I0" + String.format("%03d", id));
            }
        } 
        catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Job Card auto ID \n" + e);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

