    private void jButton_SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_SearchActionPerformed

        try {
            String sql = "SELECT Name, mobile, postcode,enddate,duedate "
                    + " FROM customer "
                    + " WHERE ID = '"+jTextField_custNo.getText()+"'";
            PreparedStatement pst = con.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

            if(rs.first())
            {
                jTextField_Name.setText(rs.getString("Name"));
                jTextField_ContactNo.setText(rs.getString("mobile"));
                jTextField_postCode.setText(rs.getString("postcode"));
                jTextField_EndDate.setText(rs.getString("enddate"));
                jTextField_DueDate.setText(rs.getString("duedate"));
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Enter a valid Customer ID..!!");
                jTextField_custNo.setText("");
            }
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null,"CMsg search \n" + e);
        }
    }//GEN-LAST:event_jButton_SearchActionPerformed

