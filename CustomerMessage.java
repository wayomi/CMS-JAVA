    public void displayTable()
    {
        try {
            String sql = "SELECT c.CustNo AS 'Customer #',c1.Name ,"
                    + " c.message AS 'Message',c.EntryTime AS 'Entry Time',c.locked AS 'Locked',"
                    + "c.lockedBy AS 'Locked By',  c.lockedTime AS 'Locked Time', c.status AS 'Status'  "
                    + "FROM cmessage c, customer c1 "
                    + "WHERE c.CustNo = c1.ID";
            
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            JTableHeader head = jTable_CustMsg.getTableHeader();
            head.setForeground(Color.blue);            
            jTable_CustMsg.getTableHeader().setFont(new Font("Times New Roman", Font.BOLD, 14));
            jTable_CustMsg.setModel(DbUtils.resultSetToTableModel(rs));
            jTable_CustMsg.setShowGrid(true); 
            
            //jTable_CustMsg.getColumnModel().getColumn(8).setPreferredWidth(1);
        } 
        catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Cmsg table \n" + e);
        }
    }
