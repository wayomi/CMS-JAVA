            p1.addCell(new PdfPCell(new Phrase("Brand",fontH1)));
            p1.addCell(new PdfPCell(new Phrase("Item Name",fontH1)));
            p1.addCell(new PdfPCell(new Phrase("Category",fontH1)));
            p1.addCell(new PdfPCell(new Phrase("Selling Price",fontH1)));
            p1.addCell(new PdfPCell(new Phrase("Cost Price",fontH1)));  
            p1.addCell(new PdfPCell(new Phrase("Quantity",fontH1))); 
            p1.addCell(new PdfPCell(new Phrase("Added Date",fontH1))); 
            
            for(int i=0; i<jTable_stock.getRowCount(); i++)
            {
                String itemNo = jTable_stock.getValueAt(i, 0).toString();
                String brand = jTable_stock.getValueAt(i, 1).toString();
                String iName = jTable_stock.getValueAt(i, 2).toString();
                String cat = jTable_stock.getValueAt(i, 3).toString();
                String sell = jTable_stock.getValueAt(i, 4).toString();
                String cost = jTable_stock.getValueAt(i, 5).toString();
                String qty = jTable_stock.getValueAt(i, 6).toString();
                String date = jTable_stock.getValueAt(i, 7).toString();              
                

                p1.addCell(new PdfPCell(new Phrase(itemNo,fontH)));
                p1.addCell(new PdfPCell(new Phrase(brand,fontH)));
                p1.addCell(new PdfPCell(new Phrase(iName,fontH)));
                p1.addCell(new PdfPCell(new Phrase(cat,fontH)));
                p1.addCell(new PdfPCell(new Phrase(sell,fontH)));
                p1.addCell(new PdfPCell(new Phrase(cost,fontH)));  
                p1.addCell(new PdfPCell(new Phrase(qty,fontH))); 
                p1.addCell(new PdfPCell(new Phrase(date,fontH))); 

            }
            doc.add(p1);
                       
            doc.close();

            JOptionPane.showMessageDialog(null, "File Downloaded..!!");    
            
