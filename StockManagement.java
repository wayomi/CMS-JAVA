    private void jButton_ItemIDNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ItemIDNewActionPerformed
        try {
            String sql = "SELECT Max(itemNo) From item";
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            rs.next();
            
            rs.getString("Max(itemNo)");
            
            if(rs.getString("Max(itemNo)")==null)
            {
                jTextField_ItemNo.setText("IT0001");
            }
            else
            {
                Long id = Long.parseLong(rs.getString("Max(itemNo)").substring(2,rs.getString("Max(itemNo)").length()));
                id++;
                jTextField_ItemNo.setText("IT0" + String.format("%03d", id));
            }
        } 
        catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Stock Auto item id \n" + e);
        }
    }//GEN-LAST:event_jButton_ItemIDNewActionPerformed

