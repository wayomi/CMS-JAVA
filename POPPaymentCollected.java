            jTextField_amt.setText(jTable_PayCollect.getValueAt(i, 2).toString()); 
            String dateValue = jTable_PayCollect.getValueAt(i, 3).toString(); 
            java.util.Date date1 = new SimpleDateFormat("yyyy-MM-dd").parse(dateValue);
            jDateChooser_Paid.setDate(date1); 
            jTextArea_Details.setText(jTable_PayCollect.getValueAt(i, 4).toString());          
