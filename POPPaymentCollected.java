    public void LoadTable()
    {
        try {
            
            String sql = "SELECT CustNo AS 'Customer #',Name, amt AS 'Amount', date AS 'Paid Date',details AS 'Details', "
                    + "type AS 'Type', payFor AS 'Pay For', collected AS 'Collected By' "
                    + " FROM paycollection ";
            
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            JTableHeader head = jTable_PayCollect.getTableHeader();
            head.setForeground(Color.blue);            
            jTable_PayCollect.getTableHeader().setFont(new Font("Times New Roman", Font.BOLD, 14));
            jTable_PayCollect.setModel(DbUtils.resultSetToTableModel(rs));
            jTable_PayCollect.setShowGrid(true); 
        } 
        catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Deposite Record load table\n" + e);
        }
    }
    private void jButton_SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_SearchActionPerformed

        try {
            String sql = "SELECT Name FROM customer WHERE ID = '"+jTextField_CustNo.getText()+"'";
            PreparedStatement pst = con.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

            if(rs.first())
            {
                jTextField_Name.setText(rs.getString("Name"));
                
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Enter a valid Customer ID..!!");
                jTextField_CustNo.setText("");
            }
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null,"POP Payment collect search \n" + e);
        }
    }//GEN-LAST:event_jButton_SearchActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String sql = "INSERT INTO paycollection (CustNo, amt, date, details, "
                    + "type, payFor, collected, Name) "
                    + "VALUES (?,?,?,?,?,?,?,?)";
            
            PreparedStatement pst = con.prepareStatement(sql);
            
            pst.setString(1, jTextField_CustNo.getText());
            pst.setString(2, jTextField_amt.getText());
            String date = sdf.format(jDateChooser_Paid.getDate());
            pst.setString(3,date);  
            pst.setString(4, jTextArea_Details.getText());
            String type = jComboBox_paytype.getSelectedItem().toString();
            pst.setString(5, type);                
            pst.setString(6, jTextField_Payfor.getText());
            pst.setString(7, jLabel_Collected.getText());
            pst.setString(8, jTextField_Name.getText());
            
            pst.executeUpdate();
          
            JOptionPane.showMessageDialog(null,"Inserted Successfully..!!");
            
            jTextField_CustNo.setText("");
            jTextArea_Details.setText("");
            jTextField_Name.setText("");
            jTextField_Payfor.setText("");
            jTextField_amt.setText("");
            jComboBox_paytype.setSelectedIndex(0);
            jDateChooser_Paid.setDate(null);  
            
            LoadTable();
            
        } 
        catch (Exception e) {
            JOptionPane.showMessageDialog(null,"POP Payment collect new \n" + e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

